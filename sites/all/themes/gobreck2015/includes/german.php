„French Gulch“ diesen Goldbagger begutachten. Ein- und
zweispurige Radwege führen dich durch das Hinterland von
Breckenridge. Du hast noch nicht genug? Überquere die Straße um zu
den Trails „X10U8“, „Side Door“ oder „Minnie Mine“ zu
gelangen. Vergiss jedoch nicht dich mit ausreichend Trinkwasser
auszustatten. Der Anstieg ist beschwerlich, aber die Abfahrt danach
ist es wert.</SPAN></SPAN></P>
